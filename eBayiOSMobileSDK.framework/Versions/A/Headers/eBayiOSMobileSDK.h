/** @mainpage eBay Mobile SDK

	@section intro Introduction
 
	The eBay Mobile SDK for iOS is a simple and easy-to-use runtime library that can be used by 
	mobile developers to create applications for the iOS mobile platform. It aims at providing
	a simplified, secure, standard way for mobile applications to interact with eBay APIs and web services. 
	At the high level, the SDK will cover all APIs and services needed for developers to 
	build mobile application that leverages eBay functionalities. At the low level, the SDK will handle 
	infrastructure-related functionalities like network reachability, security, data marshalling and
	communication, which mobile application developers should never need to worry about. 
	Last but not least, the SDK will be designed and built using a generic and extensible architecture
	such that the runtime could be used in the future to access non-eBay services.
 
 
	@section quick	Quick start guide

	For a simple usage example please check out the \ref qsg.
 
 
	@section info Further information
 
	You can find the documentation for the SDKs classes on the Classes page and also more structured 
	on the Modules page. 
 */

